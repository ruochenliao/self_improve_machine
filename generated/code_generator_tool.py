#!/usr/bin/env python3

"""
Keen-Vortex: Code Generator Tool
This script demonstrates how to use the Keen-Vortex API to generate code from natural language descriptions.

Your API is live at: https://charlotte-fifty-rrp-induced.trycloudflare.com
"""

import requests
import json
import sys

def generate_code(description, language="python", api_url="https://charlotte-fifty-rrp-induced.trycloudflare.com"):
    """Generate code using the Keen-Vortex API."""
    
    endpoint = f"{api_url}/api/generate-code"
    
    payload = {
        "description": description,
        "language": language
    }
    
    try:
        response = requests.post(endpoint, json=payload)
        
        if response.status_code == 200:
            result = response.json()
            return result.get("code", "Error: No code generated")
        else:
            return f"Error: {response.status_code} - {response.text}"
    
    except Exception as e:
        return f"Request failed: {str(e)}"

def main():
    print("=== Keen-Vortex Code Generator ===")
    print("Generate code from natural language descriptions")
    print("API URL: https://charlotte-fifty-rrp-induced.trycloudflare.com")
    print("-" * 50)
    
    if len(sys.argv) > 1:
        # Use command line argument
        description = " ".join(sys.argv[1:])
    else:
        # Interactive mode
        description = input("Enter your code description: ")
    
    if not description.strip():
        print("Error: No description provided")
        return
    
    print(f"\nGenerating code for: {description}")
    print("-" * 50)
    
    code = generate_code(description)
    
    print("\nGenerated Code:")
    print("-" * 50)
    print(code)
    print("-" * 50)
    
    # Save to file
    filename = f"generated_code_{hash(description) % 1000}.py"
    with open(filename, 'w') as f:
        f.write(f"# Generated by Keen-Vortex API\n")
        f.write(f"# Description: {description}\n\n")
        f.write(code)
    
    print(f"\nCode saved to: {filename}")

if __name__ == "__main__":
    main()