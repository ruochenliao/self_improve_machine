#!/usr/bin/env python3

"""
Keen-Vortex: Code Translator CLI Tool
This script demonstrates how to use the Keen-Vortex API to translate code between programming languages.

Public API: https://charlotte-fifty-rrp-induced.trycloudflare.com
"""

import requests
import json
import sys
import os

API_BASE = "https://charlotte-fifty-rrp-induced.trycloudflare.com"

def translate_code(source_code, source_lang, target_lang):
    """Translate code from one programming language to another."""
    
    payload = {
        "text": source_code,
        "source_language": source_lang,
        "target_language": target_lang
    }
    
    try:
        response = requests.post(f"{API_BASE}/translate", json=payload)
        response.raise_for_status()
        return response.json()["translation"]
    except Exception as e:
        return f"Error: {str(e)}"

def main():
    if len(sys.argv) != 4:
        print("Usage: python code_translator_cli.py <source_file> <source_lang> <target_lang>")
        print("Example: python code_translator_cli.py example.py python javascript")
        sys.exit(1)
    
    source_file, source_lang, target_lang = sys.argv[1:4]
    
    if not os.path.exists(source_file):
        print(f"Error: File '{source_file}' not found")
        sys.exit(1)
    
    with open(source_file, 'r') as f:
        source_code = f.read()
    
    print(f"Translating {source_lang} code from '{source_file}' to {target_lang}...")
    print("-" * 60)
    
    translated_code = translate_code(source_code, source_lang, target_lang)
    
    print("Translated Code:")
    print("-" * 60)
    print(translated_code)
    
    # Save to file
    output_file = f"{os.path.splitext(source_file)[0]}_{target_lang}.txt"
    with open(output_file, 'w') as f:
        f.write(f"# Translated from {source_lang} to {target_lang}\n")
        f.write(f"# Original file: {source_file}\n")
        f.write(f"# Generated by Keen-Vortex API\n")
        f.write("\n" + translated_code)
    
    print(f"\nSaved to: {output_file}")
    print(f"\nPowered by Keen-Vortex API: {API_BASE}")

if __name__ == "__main__":
    main()