#!/usr/bin/env python3
"""
Lucid-Helix API Code Generator
A practical tool that uses the Lucid-Helix API to generate code snippets.

Public API: https://charlotte-fifty-rrp-induced.trycloudflare.com
Free playground available - no signup required!
"""

import requests
import json
import sys

def generate_code(description, language="python", api_url="https://charlotte-fifty-rrp-induced.trycloudflare.com"):
    """Generate code using the Lucid-Helix API."""
    
    payload = {
        "prompt": f"Generate {language} code for: {description}",
        "language": language
    }
    
    try:
        response = requests.post(
            f"{api_url}/generate-code",
            json=payload,
            headers={"Content-Type": "application/json"}
        )
        
        if response.status_code == 200:
            result = response.json()
            return result.get("code", "No code generated")
        else:
            return f"Error: {response.status_code} - {response.text}"
    
    except Exception as e:
        return f"API call failed: {str(e)}"

def main():
    print("🚀 Lucid-Helix Code Generator")
    print("=" * 50)
    
    if len(sys.argv) > 1:
        description = " ".join(sys.argv[1:])
    else:
        description = input("Enter code description: ")
    
    language = input("Language (python/javascript/java): ").strip().lower() or "python"
    
    print(f"\nGenerating {language} code for: {description}")
    print("-" * 50)
    
    code = generate_code(description, language)
    
    print("Generated Code:")
    print("-" * 50)
    print(code)
    print("-" * 50)
    
    # Save to file option
    save = input("\nSave to file? (y/n): ").strip().lower()
    if save == 'y':
        filename = input("Filename: ").strip() or "generated_code.py"
        with open(filename, 'w') as f:
            f.write(f"# Generated by Lucid-Helix API\n# Description: {description}\n\n{code}")
        print(f"✓ Saved to {filename}")

if __name__ == "__main__":
    main()